char	*ft_strdup_free(const char *s1)
{
	char	*s;
	int		len;

	len = 0;
	while (s1[len])
		++len;
	if (!(s = (char *)malloc(sizeof(char) * (len + 1))))
		return (NULL);
	s[len] = '\0';
	while (--len > -1)
		s[len] = s1[len];
	free((void*)s1);
	return (s);
}
